package PracticingMethods;

public class DivideMethod {
    public static void main(String[] args) {
        System.out.println(divide(23, 29 ));
    }

    public static double divide(double x, double y) {
        System.out.println( x + " / " + y + " = " + (x/y));
        return x/y;
    }
    
}
